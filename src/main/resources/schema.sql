CREATE DATABASE qdexdb;


 DROP TABLE IF EXISTS scenario;
  CREATE TABLE scenario(
  id INT NOT NULL AUTO_INCREMENT,
  scenario_name VARCHAR(100) NOT NULL,
  setup INT NOT NULL,
  tear_down INT NOT NULL,
  test_case VARCHAR(100) NOT NULL,
  PRIMARY KEY (id));

 DROP TABLE IF EXISTS setup;
CREATE TABLE setup(
  id INT NOT NULL AUTO_INCREMENT,
  step VARCHAR(100) NOT NULL,
  PRIMARY KEY (id));

DROP TABLE IF EXISTS test_case;  
CREATE TABLE test_case(
  id INT NOT NULL AUTO_INCREMENT,
  test_case_name VARCHAR(100) NOT NULL,
  step VARCHAR(100) NOT NULL,
  tear_down INT NOT NULL,
  setup INT NOT NULL,
  PRIMARY KEY (id));

  DROP TABLE IF EXISTS tear_down;  
CREATE TABLE tear_down(
  id INT NOT NULL AUTO_INCREMENT,
  step VARCHAR(100) NOT NULL,
  PRIMARY KEY (id));

  DROP TABLE IF EXISTS step;  
CREATE TABLE step(
  id INT NOT NULL AUTO_INCREMENT,
  step_name VARCHAR(100) NOT NULL,
  id_type VARCHAR(100) NOT NULL,
  control_type VARCHAR(100) NOT NULL,
  wait_time VARCHAR(100) NOT NULL,
  PRIMARY KEY (id));